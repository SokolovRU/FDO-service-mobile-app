export const AuthorizationComp = () => {
  const application = useAppSelector((state) => state);

  const [activeReq, setActiveReq] = useState(false);
  function changeActiveReq(value: boolean) {
    setActiveReq(value);
  }
  // changing the displayed screen
  const [typeScreen, setTypeScreen] = useState("login");
  function changeTypeScreen(
    type: "login" | "register" | "recovery" | "confirmation"
  ) {
    setTypeScreen(type);
  }

  function actionBack() {
    if (typeScreen === "register") {
      changeTypeScreen("login");
      LayoutAnimation.configureNext({
        duration: 300,
        create: {
          type: LayoutAnimation.Types.easeInEaseOut,
          property: LayoutAnimation.Properties.opacity,
        },
        update: {
          type: LayoutAnimation.Types.easeInEaseOut,
        },
      });
    } else if (typeScreen === "confirmation") {
      changeTypeScreen("register");
      LayoutAnimation.configureNext({
        duration: 300,
        create: {
          type: LayoutAnimation.Types.easeInEaseOut,
          property: LayoutAnimation.Properties.opacity,
        },
        update: {
          type: LayoutAnimation.Types.easeInEaseOut,
        },
      });
    } else if (typeScreen === "recovery") {
      changeTypeScreen("login");
      LayoutAnimation.configureNext({
        duration: 300,
        create: {
          type: LayoutAnimation.Types.easeInEaseOut,
          property: LayoutAnimation.Properties.opacity,
        },
        update: {
          type: LayoutAnimation.Types.easeInEaseOut,
        },
      });
    } else {
      changeTypeScreen("login");
    }
  }

  // objects that store information on inputs
  const [loginInfo, setInfoLogin] = useState({
    login: "",
    password: "",
  });
  const [registerInfo, setRegisterLogin] = useState({
    login: "",
    password: "",
    repeatPassword: "",
    email: "",
    group: "",
  });
  const [confirmationInfo, setConfirmationInfo] = useState({
    name: "",
    surname: "",
    patronymic: "",
    card: "",
    photo: "",
  });

  // functions that are able to edit objects
  function changeLoginInfo(login: string, password: string) {
    setInfoLogin({
      login: login,
      password: password,
    });
  }
  function changeRegisterInfo(
    login: string,
    password: string,
    repeatPassword: string,
    email: string,
    group: string
  ) {
    setRegisterLogin({
      login: login,
      password: password,
      repeatPassword: repeatPassword,
      email: email,
      group: group,
    });
  }
  function changeConfirmationInfo(
    name: string,
    surname: string,
    patronymic: string,
    card: string,
    photo: any
  ) {
    setConfirmationInfo({
      name: name,
      surname: surname,
      patronymic: patronymic,
      card: card,
      photo: photo,
    });
  }

  const RetMainComp = () => {
    if (typeScreen === "login") {
      return (
        <LoginComp
          loginInfo={loginInfo}
          changeLoginInfo={changeLoginInfo}
          changeTypeScreen={changeTypeScreen}
          changeActiveReq={changeActiveReq}
        />
      );
    } else if (typeScreen === "register") {
      return (
        <RegisterComp
          registerInfo={registerInfo}
          changeRegisterInfo={changeRegisterInfo}
          changeTypeScreen={changeTypeScreen}
          changeActiveReq={changeActiveReq}
        />
      );
    } else if (typeScreen === "confirmation") {
      return (
        <ConfirmationComp
          confirmationInfo={confirmationInfo}
          changeConfirmationInfo={changeConfirmationInfo}
          changeTypeScreen={changeTypeScreen}
          changeActiveReq={changeActiveReq}
          registerInfo={registerInfo}
        />
      );
    } else if (typeScreen === "recovery") {
      return <RecoveryComp changeTypeScreen={changeTypeScreen}/>;
    } else {
      return (
        <LoginComp
          loginInfo={loginInfo}
          changeLoginInfo={changeLoginInfo}
          changeTypeScreen={changeTypeScreen}
          changeActiveReq={changeActiveReq}
        />
      );
    }
  };

  return (
      <TouchableWithoutFeedback onPress={Keyboard.dismiss} accessible={false}>
        <SafeAreaView style={{ flex: 1, backgroundColor: "#fff" }}>
          {!activeReq ? (
            <></>
          ) : (
            <View
              style={{
                backgroundColor: "rgba(0, 0, 0, 0.7)",
                alignItems: "center",
                justifyContent: "center",
                position: "absolute",
                height: Dimensions.get("window").height + 50,
                width: Dimensions.get("window").width,
                flex: 1,
                zIndex: 10,
              }}
            >
              <ActivityIndicator
                color={Colors.Highlight.light}
                size={"large"}
              />
            </View>
          )}
          <View style={{ flex: 1, backgroundColor: Colors.Highlight.lightest }}>
            {typeScreen !== "login" ? (
              <View
                style={{
                  position: "absolute",
                  top: vs(24),
                  left: s(16),
                  zIndex: 3,
                }}
              >
                <TouchableOpacity onPress={() => actionBack()}>
                  <LeftBackSvg />
                </TouchableOpacity>
              </View>
            ) : (
              <></>
            )}
            <View style={{ position: "absolute", top: vs(80), left: s(16) }}>
              <BackSvg />
            </View>
            <View style={{ flex: 1 }}></View>
            <RetMainComp />
          </View>
        </SafeAreaView>
      </TouchableWithoutFeedback>
  );
};

